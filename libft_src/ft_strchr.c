#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = -1;
	while (s[++i] != '\0')
		if (s[i] == (char)c)
			return ((char *)(s + i));
	if ((char)c == '\0')
		return ((char *)(s + i));
	return (NULL);
}
